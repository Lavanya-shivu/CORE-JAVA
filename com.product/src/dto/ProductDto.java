package dto;

public class ProductDto {
    public String name;
    public String type;
    public String incomingPrice;
    public String sellingPrice;
    public String mrp;
    public String description;
    public String brand;
    public String quantity;
    public String color;
    public String weight;
    public String manfDate;
    public String warranty;
    public String returnPolicy;

    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name=name;
    }
    public String getType(){
        return type;
    }
    public void setType(String type){
        this.type=type;
    }
    public String getIncomingPrice(){
        return incomingPrice;
    }
    public void setIncomingPrice(String incomingPrice){
        this.incomingPrice=incomingPrice;
    }
    public String getSellingPrice(){
        return sellingPrice;
    }
    public void setSellingPrice(String sellingPrice){
        this.sellingPrice=sellingPrice;
    }
    public String getMrp(){
        return mrp;
    }
    public void setMrp(String mrp){
        this.mrp=mrp;
    }
    public String getDescription(){
        return description;
    }
    public void setDescription(String description){
        this.description=description;
    }
    public String getBrand(){
        return brand;
    }
    public void setBrand(String brand){
        this.brand=brand;
    }
    public String getQuantity(){
        return quantity;
    }
    public void setQuantity(String quantity){
        this.quantity=quantity;
    }
    public String getColor(){
        return color;
    }
    public void setColor(String color){
        this.color=color;
    }
    public String getWeight(){
        return weight;
    }
    public void setWeight(String weight){
        this.weight=weight;
    }
    public String getManfDate(){
        return manfDate;
    }
    public void setManfDate(String manfDate){
        this.manfDate=manfDate;
    }
    public String getWarranty(){
        return warranty;
    }
    public void setWarranty(String warranty){
        this.warranty=warranty;
    }
    public String getReturnPolicy(){
        return returnPolicy;
    }
    public void setReturnPolicy(String returnPolicy){
        this.returnPolicy=returnPolicy;
    }
}
